//Arithmetic Operators (works on two or more operands)
let a = 4;
let b = 6;
console.log("a =", a , "b =", b);
console.log("a+b=", a + b);
console.log("a-b=", a-b);
console.log("a*b=", a*b);
console.log("a/b=", a/b);
console.log("a%b=", a%b);
console.log("a**b=", a**b); //a^b
//Unary Operators (works on single operands)
console.log("a-- =" , a--); //postfix decreement 
console.log(a);
console.log("++b = ", ++b); //prefix increment
console.log(b);
